#Day of the Week
num = int(input('Please enter a number between 1 and seven: '))
if num == 1:
	print('It is Monday')
elif num == 2:
	print('It is Tuesday')
elif num == 3:
	print('It is Wednesday')
elif num == 4:
	print('It is Thurdsay')
elif num == 5:
	print('It is Friday')
elif num == 6:
	print('It is Saturday')
elif num == 7:
	print('It is Sunday')
else:
	print('Error: Number not within range. Please try again.')


